package com.example.mymusicmanager.Data;

public class Buttons {
    public static void onClickStart(boolean state){
        state = true;
    }

    public static void onClickStop(boolean state){
        state = true;
    }

    public static void onClickReset(boolean state, int senconds){
        state = true;
        senconds = 0;
    }
}
